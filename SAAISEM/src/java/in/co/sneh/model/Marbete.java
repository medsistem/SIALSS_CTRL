package in.co.sneh.model;

/**
 *
 * @author HP-MEDALFA
 */
public class Marbete {

    private String claveDocumento;
    private String cb;
    private String clave;
    private String descripcion;
    private String lote;
    private String fecCaducidad;
    private String ordenCompra;
    private String fecElaboracion;
    private Integer piezas;

    public String getClaveDocumento() {
        return claveDocumento;
    }

    public void setClaveDocumento(String claveDocumento) {
        this.claveDocumento = claveDocumento;
    }

    public String getCb() {
        return cb;
    }

    public void setCb(String cb) {
        this.cb = cb;
    }

    public String getClave() {
        return clave;
    }

    public void setClave(String clave) {
        this.clave = clave;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getLote() {
        return lote;
    }

    public void setLote(String lote) {
        this.lote = lote;
    }

    public String getFecCaducidad() {
        return fecCaducidad;
    }

    public void setFecCaducidad(String fecCaducidad) {
        this.fecCaducidad = fecCaducidad;
    }

    public String getOrdenCompra() {
        return ordenCompra;
    }

    public void setOrdenCompra(String ordenCompra) {
        this.ordenCompra = ordenCompra;
    }

    public String getFecElaboracion() {
        return fecElaboracion;
    }

    public void setFecElaboracion(String fecElaboracion) {
        this.fecElaboracion = fecElaboracion;
    }

    public Integer getPiezas() {
        return piezas;
    }

    public void setPiezas(Integer piezas) {
        this.piezas = piezas;
    }

    
}
